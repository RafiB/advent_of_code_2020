def search_for_seamonsters(image):
    for y, row in enumerate(image):
        for x, cell in enumerate(image):
            for seamonster in SEAMONSTERS:
                match_seamonster(seamonster, image, y, x)


def match_seamonster(seamonster, image, y, x):

    match = True
    for s_y, row in enumerate(seamonster):
        for s_x, c in enumerate(row):
            if y + s_y >= len(image):
                match = False
                break
            if x + s_x >= len(image[0]):
                match = False
                break
            if seamonster[s_y][s_x] == "#":
                if image[y+s_y][x+s_x] not in ("#", "O"):
                    match = False
                    break
        if not match:
            break

    if match:
        for s_y, row in enumerate(seamonster):
            for s_x, c in enumerate(row):
                if seamonster[s_y][s_x] == "#":
                    image[y+s_y][x+s_x] = "O"


def matches_left(board, coord, candidate):
    if coord.x == 0:
        return True
    left_neighbour = board[coord.y][coord.x-1].tile

    # print("does", candidate, "match", left_neighbour, "to the left")

    for match_row, row in zip(left_neighbour, candidate):
        if match_row[-1] != row[0]:
            return False

    return True


def matches_up(board, coord, candidate):
    if coord.y == 0:
        return True

    up_neighbour = board[coord.y-1][coord.x].tile

    return candidate[0] == up_neighbour[-1]


