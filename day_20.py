def rotate(tile):
    """
    #..
    ..#
    ###
    ->
    #.#
    #..
    ##.
    """
    if len(tile) == 1:
        return tile

    if len(tile) == 2:
        return [
            [tile[1][0], tile[0][0]],
            [tile[1][1], tile[0][1]]
        ]

    rotated_tile = [['.' for c in range(len(tile[0]))] for r in range(len(tile))]

    for i, row in enumerate(tile):
        rotated_tile[0][i] = tile[len(tile)-1-i][0]
        rotated_tile[-1][len(tile)-1-i] = row[-1]

    for i, cell in enumerate(tile[0]):
        rotated_tile[i][-1] = cell

    for i, cell in enumerate(tile[-1]):
        rotated_tile[i][0] = cell

    inside = rotate(tuple(row[1:-1] for row in tile[1:-1]))
    for r in range(1, len(tile)-1):
        for c in range(1, len(tile[0])-1):
            rotated_tile[r][c] = inside[r-1][c-1]

    return tuple("".join(row) for row in rotated_tile)


def all_rotations_of_tile(tile):
    rotations = [tile]
    rotations.append(rotate(rotations[-1]))
    rotations.append(rotate(rotations[-1]))
    rotations.append(rotate(rotations[-1]))
    return rotations


def flip(tile):
    flipped_tiles = [tile]

    # flip horizontally
    flipped_tiles.append(tuple(reversed(tile)))

    # flip vertically
    flipped_vert = []
    for row in tile:
        flipped_vert.append("".join(list(reversed(row))))

    flipped_tiles.append(tuple(flipped_vert))
    return flipped_tiles

def solve(tile_defs):
    curr_tile = ""

    tiles = {}

    for line in tile_defs.split("\n"):
        if not line:
            continue
        if line.startswith("Tile "):
            curr_tile = line.split(" ")[1][:-1]
            tiles[int(curr_tile)] = []
        else:
            tiles[int(curr_tile)].append(line)

    for tile_id in tiles:
        tiles[tile_id] = tuple(tiles[tile_id])

    board_dimension = int(len(tiles)**0.5)

    board = tuple(tuple(None for c in range(board_dimension)) for r in range(board_dimension))
    print(f"{board=}")

    print(f"there are {len(tiles)} in the grid")

    first_coord = Coord(x=0, y=0)
    placed_tiles = _place_tiles(board, tiles, first_coord, seen=set())



    image = []
    for tile_row in placed_tiles:
        for i, meta_row in enumerate(zip(*[t.tile for t in tile_row])):
            line = ""
            if i == 0:
                continue
            if i == len(placed_tiles[0][0].tile) - 1:
                continue
            for row in meta_row:
                line += row[1:-1]
            image.append(list(line))

    search_for_seamonsters(image)

    count = 0
    for row in image:
        for cell in row:
            if cell == "#":
                count += 1
    return count


